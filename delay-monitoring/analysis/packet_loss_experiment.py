"""
Packet loss experiment — analysis script.

Reads the CSVs produced by run_packet_loss_experiment.sh, where each
CSV was generated by an NS-3 simulation that applied independent
Bernoulli packet loss at the given rate before recording.  The CSV
therefore contains only the delays of packets that were NOT lost —
i.e. exactly what a passive monitor would observe.

For each (distribution, loss_rate, n_transmitted):
  - loads the CSV  (n_received rows, where n_received <= n_transmitted)
  - builds the empirical Probability Mass Function (PMF)
  - computes Total Variation Distance (TVD) against the true distribution

Two key plots per distribution:
  1. TVD vs n_transmitted  — shows accuracy degrading as loss rate rises
     for the same transmitted budget.
  2. TVD vs n_received     — should collapse to a single curve for all
     loss rates, proving that random loss is equivalent to reducing the
     effective sample count.  The correction is simply:
         n_transmitted >= n_theory / (1 - p)

Summary plot across all distributions:
  - Required transmitted packets vs loss rate (empirical, vs theoretical
    1/(1-p) curve).

Results saved to: results/packet-loss/
"""

import math
import os

import matplotlib.ticker as mticker
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from scipy import stats

# ---------------------------------------------------------------------------
# parameters — must match run_packet_loss_experiment.sh
# ---------------------------------------------------------------------------

BIN_SIZE = 1        # ms
GRID_MAX = 150.0
EPSILON  = 0.05
DELTA    = 0.05

K        = int(GRID_MAX / BIN_SIZE)                                  # 150
n_theory = math.ceil((K + math.log(1.0 / EPSILON)) / DELTA ** 2)    # 61,199

LOSS_RATES         = [0.0, 0.1, 0.25, 0.5, 0.75]
TRANSMITTED_COUNTS = [n_theory // 4, n_theory // 2, n_theory, n_theory * 2]
TRANSMITTED_LABELS = ["n/4",          "n/2",         "n",      "2n"]

SOURCE_DIR  = "../results/packet-loss"
RESULTS_DIR = "../results/packet-loss"

DISTRIBUTIONS = {
    "normal": {
        "label":  "Normal",
        "params": {"mean": 40.0, "variance": 4.0},
        "color":  "#3498db",
    },
    "lognormal": {
        "label":  "Lognormal",
        "params": {"mu": 2.3, "sigma": 0.2},
        "color":  "#e74c3c",
    },
    "weibull": {
        "label":  "Weibull",
        "params": {"scale": 10.0, "shape": 2.0},
        "color":  "#2ecc71",
    },
}

LOSS_COLORS  = {0.0: "#333333", 0.1: "#3498db", 0.25: "#f39c12",
                0.5: "#e74c3c", 0.75: "#9b59b6"}
LOSS_MARKERS = {0.0: "o",       0.1: "s",        0.25: "^",
                0.5: "D",       0.75: "P"}

BIN_EDGES   = np.arange(0.0, GRID_MAX + BIN_SIZE, BIN_SIZE)
BIN_CENTRES = BIN_EDGES[:-1] + BIN_SIZE / 2.0


# ---------------------------------------------------------------------------
# helpers
# ---------------------------------------------------------------------------

def load_samples(loss_rate, dist_name, n_transmitted):
    """Load received-packet delays from the NS-3 output CSV."""
    loss_pct = int(loss_rate * 100)
    path = os.path.join(SOURCE_DIR,
                        f"loss_{loss_pct}pct",
                        f"delay_samples_{dist_name}_{n_transmitted}.csv")
    return pd.read_csv(path)["delay_ms"].values


def true_pmf(dist_name, params):
    """True probability mass per 1ms bin via CDF differences."""
    if dist_name == "normal":
        cdf = stats.norm.cdf(BIN_EDGES, loc=params["mean"],
                             scale=np.sqrt(params["variance"]))
    elif dist_name == "lognormal":
        cdf = stats.lognorm.cdf(BIN_EDGES, s=params["sigma"],
                                scale=np.exp(params["mu"]))
    elif dist_name == "weibull":
        cdf = stats.weibull_min.cdf(BIN_EDGES, c=params["shape"],
                                    scale=params["scale"])
    masses = np.diff(cdf)
    return masses / masses.sum()


def empirical_pmf(samples, smoothing=0.5):
    """Empirical PMF with Laplace smoothing for unobserved bins."""
    counts = np.zeros(K)
    idx    = np.floor(samples / BIN_SIZE).astype(int)
    valid  = (idx >= 0) & (idx < K)
    np.add.at(counts, idx[valid], 1)
    smoothed = counts + smoothing
    return smoothed / smoothed.sum()


def tvd(p, q):
    """Total Variation Distance."""
    return 0.5 * float(np.sum(np.abs(p - q)))


# ---------------------------------------------------------------------------
# plotting
# ---------------------------------------------------------------------------

def _fix_log_xticks(ax, values, labels):
    ax.xaxis.set_major_locator(mticker.FixedLocator(values))
    ax.xaxis.set_major_formatter(mticker.FixedFormatter(labels))
    ax.xaxis.set_minor_formatter(mticker.NullFormatter())


def plot_tvd_vs_n(dist_name, dist_config, results):
    """
    Two panels:
      Left:  TVD vs n_transmitted — shows that higher loss needs more
             transmitted packets to reach the same accuracy.
      Right: TVD vs n_received   — curves should collapse, showing that
             random loss is equivalent to reducing the effective sample count.
    """
    fig, axes = plt.subplots(1, 2, figsize=(13, 5))
    fig.suptitle(
        f"{dist_config['label']} — Total Variation Distance (TVD) under random packet loss\n"
        f"n_theory = {n_theory:,}  (1ms bins, k = {K}, ε = {EPSILON}, δ = {DELTA})",
        fontsize=11
    )

    for p, records in results.items():
        color  = LOSS_COLORS[p]
        marker = LOSS_MARKERS[p]
        label  = f"loss = {int(p * 100)}%"
        n_tx   = [r["n_tx"]   for r in records]
        n_rx   = [r["n_rx"]   for r in records]
        tvds   = [r["tvd"]    for r in records]

        axes[0].plot(n_tx, tvds, color=color, marker=marker, linestyle="-",
                     linewidth=2, markersize=7, label=label)
        axes[1].plot(n_rx, tvds, color=color, marker=marker, linestyle="-",
                     linewidth=2, markersize=7, label=label)

    for ax, title, xlabel in [
        (axes[0],
         "TVD vs packets transmitted\n"
         "(same budget → worse accuracy as loss rate rises)",
         "Packets transmitted"),
        (axes[1],
         "TVD vs packets received (after loss)\n"
         "(curves collapse — random loss = effective sample reduction)",
         "Packets received"),
    ]:
        ax.axvline(x=n_theory, color="grey", linestyle=":", linewidth=1.5,
                   label=f"n_theory = {n_theory:,}")
        ax.set_xscale("log")
        _fix_log_xticks(ax,
                        TRANSMITTED_COUNTS,
                        [f"{n:,}" for n in TRANSMITTED_COUNTS])
        ax.tick_params(axis="x", labelrotation=20)
        ax.set_xlabel(xlabel)
        ax.set_ylabel("Total Variation Distance (TVD)")
        ax.set_title(title, fontsize=9)
        ax.legend(fontsize=8)
        ax.grid(True, alpha=0.3, which="both")

    plt.tight_layout()
    out = os.path.join(RESULTS_DIR, f"tvd_vs_n_{dist_name}.png")
    plt.savefig(out, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  Saved: {out}")


def plot_summary(all_results):
    """
    Required transmitted packets vs loss rate for each distribution,
    overlaid with the theoretical n_theory / (1-p) curve.
    A data point is plotted for each loss rate where the observed TVD
    at the given n_transmitted first falls at or below the no-loss TVD
    at n_theory (with 20% slack for sampling variance).
    """
    fig, ax = plt.subplots(figsize=(8, 5))
    fig.suptitle(
        "Random packet loss — transmitted packets required to match no-loss accuracy\n"
        f"1ms bins, k = {K}, ε = {EPSILON}, δ = {DELTA}, n_theory = {n_theory:,}",
        fontsize=11
    )

    for dist_name, dist_config in DISTRIBUTIONS.items():
        # reference: no-loss TVD at n_theory transmitted
        ref = next(r for r in all_results[dist_name][0.0]
                   if r["n_tx"] == n_theory)
        target = ref["tvd"] * 1.2   # 20% slack for stochastic variation

        valid_p, n_req = [], []
        for p in LOSS_RATES:
            for r in sorted(all_results[dist_name][p], key=lambda x: x["n_tx"]):
                if r["tvd"] <= target:
                    valid_p.append(p)
                    n_req.append(r["n_tx"])
                    break

        if n_req:
            ax.plot(valid_p, n_req, color=dist_config["color"], marker="o",
                    linewidth=2, markersize=8, label=dist_config["label"])

    # theoretical curve
    ps = np.linspace(0.0, 0.74, 300)
    ax.plot(ps, n_theory / (1.0 - ps), "k--", linewidth=1.5,
            label="Theoretical: n_theory / (1 − p)")

    ax.set_xlabel("Loss rate p")
    ax.set_ylabel("Packets transmitted required")
    ax.set_title("Random loss budget: scales as n_theory / (1 − p)", fontsize=9)
    ax.legend(fontsize=9)
    ax.grid(True, alpha=0.3)

    plt.tight_layout()
    out = os.path.join(RESULTS_DIR, "loss_summary.png")
    plt.savefig(out, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  Saved: {out}")


# ---------------------------------------------------------------------------
# main
# ---------------------------------------------------------------------------

def run():
    print("=" * 65)
    print("Packet loss experiment — analysis")
    print("=" * 65)
    print(f"  1ms bins, k = {K},  n_theory = {n_theory:,}")
    print(f"  Loss rates     : {[f'{int(p*100)}%' for p in LOSS_RATES]}")
    print(f"  n_transmitted  : {TRANSMITTED_COUNTS}")
    print("=" * 65)
    print()

    # all_results[dist_name][loss_rate] = list of {n_tx, n_rx, tvd}
    all_results = {d: {p: [] for p in LOSS_RATES} for d in DISTRIBUTIONS}

    header = (f"{'Distribution':<12} {'Loss':>6}  {'n_tx':>8}  "
              f"{'n_rx':>8}  {'actual loss':>12}  {'TVD':>10}")
    print(header)
    print("-" * len(header))

    for dist_name, dist_config in DISTRIBUTIONS.items():
        true_pmf_vals = true_pmf(dist_name, dist_config["params"])

        for p in LOSS_RATES:
            first = True
            for n_tx, lbl in zip(TRANSMITTED_COUNTS, TRANSMITTED_LABELS):
                samples = load_samples(p, dist_name, n_tx)
                n_rx    = len(samples)
                actual_loss = 1.0 - n_rx / n_tx if n_tx > 0 else 0.0

                pmf  = empirical_pmf(samples)
                tvd_val = tvd(true_pmf_vals, pmf)

                all_results[dist_name][p].append(
                    {"n_tx": n_tx, "n_rx": n_rx, "tvd": tvd_val}
                )

                d_col = dist_name if first else ""
                p_col = f"{int(p*100)}%" if first else ""
                first = False
                print(f"{d_col:<12} {p_col:>6}  "
                      f"{lbl:>4} ({n_tx:>7,})  "
                      f"{n_rx:>8,}  {actual_loss:>11.1%}  {tvd_val:>10.5f}")

            print()

    print("Generating plots...")
    for dist_name, dist_config in DISTRIBUTIONS.items():
        plot_tvd_vs_n(dist_name, dist_config, all_results[dist_name])

    plot_summary(all_results)
    print("Done.")


if __name__ == "__main__":
    run()
