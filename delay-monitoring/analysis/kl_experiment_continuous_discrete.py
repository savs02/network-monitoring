"""
KL divergence and TVD experiment — discrete approximation of continuous distributions.

Each delay sample is rounded to the nearest millisecond, giving an integer-valued
empirical PMF at 1ms resolution. This avoids KDE entirely: the monitor simply counts
how many packets fell in each 1ms bin and normalises.

The true reference distribution is also discretised: the true PDF integrated over
each 1ms bin gives the true probability mass per bin.

This experiment reuses the CSVs generated by run_sample_complexity.sh.
Results are saved to results/sample-complexity-continuous-discrete/.
"""

import math
import os
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from scipy import stats

# ---------------------------------------------------------------------------
# parameters — must match run_sample_complexity.sh
# ---------------------------------------------------------------------------

GRID_MIN  = 0.0
GRID_MAX  = 150.0
BIN_WIDTH = 1.0

# integer bin edges and centres
BIN_EDGES   = np.arange(GRID_MIN, GRID_MAX + BIN_WIDTH, BIN_WIDTH)   # 0, 1, ..., 150
BIN_CENTRES = np.arange(GRID_MIN + BIN_WIDTH / 2, GRID_MAX, BIN_WIDTH)  # 0.5, 1.5, ...
K           = len(BIN_CENTRES)  # 150

EPSILON = 0.05
DELTA   = 0.05

n_theory = math.ceil((K + math.log(1.0 / EPSILON)) / (DELTA ** 2))

SAMPLE_SIZES = [
    n_theory // 4,
    n_theory // 2,
    n_theory,
    n_theory * 2,
]

# reuse CSVs from the continuous experiment
SOURCE_DIR  = "../results/sample-complexity"
RESULTS_DIR = "../results/sample-complexity-continuous-discrete"

DISTRIBUTIONS = {
    "normal": {
        "label": "Normal",
        "params": {"mean": 40.0, "variance": 4.0},
        "color": "#3498db",
    },
    "lognormal": {
        "label": "Lognormal",
        "params": {"mu": 2.3, "sigma": 0.2},
        "color": "#e74c3c",
    },
    "weibull": {
        "label": "Weibull",
        "params": {"scale": 10.0, "shape": 2.0},
        "color": "#2ecc71",
    },
}

# ---------------------------------------------------------------------------
# helpers
# ---------------------------------------------------------------------------

def print_parameters():
    print("=" * 55)
    print("Continuous-discrete experiment parameters")
    print("=" * 55)
    print(f"  Grid        : {GRID_MIN}–{GRID_MAX} ms at {BIN_WIDTH} ms resolution")
    print(f"  k (bins)    : {K}")
    print(f"  epsilon     : {EPSILON}")
    print(f"  delta       : {DELTA}")
    print(f"  n_theory    : {n_theory}")
    print(f"  Sample sizes: {SAMPLE_SIZES}  (n/4, n/2, n, 2n)")
    print(f"  Estimator   : empirical PMF (rounded samples, no KDE)")
    print("=" * 55)
    print()


def load_samples(dist_name, n):
    filepath = os.path.join(SOURCE_DIR, f"delay_samples_{dist_name}_{n}.csv")
    return pd.read_csv(filepath)["delay_ms"].values


def true_pmf(dist_name, params):
    """
    True probability mass per bin: integrate the PDF over each 1ms bin.
    This is the honest discrete reference — what the true distribution assigns
    to each integer millisecond interval.
    """
    if dist_name == "normal":
        cdf = stats.norm.cdf(BIN_EDGES, loc=params["mean"], scale=np.sqrt(params["variance"]))
    elif dist_name == "lognormal":
        cdf = stats.lognorm.cdf(BIN_EDGES, s=params["sigma"], scale=np.exp(params["mu"]))
    elif dist_name == "weibull":
        cdf = stats.weibull_min.cdf(BIN_EDGES, c=params["shape"], scale=params["scale"])
    else:
        raise ValueError(f"Unknown distribution: {dist_name}")

    masses = np.diff(cdf)  # probability in each bin = CDF(right edge) - CDF(left edge)
    return masses / masses.sum()


def empirical_pmf(samples, smoothing=0.5):
    """
    Empirical PMF: round samples to nearest integer ms, count per bin.
    Laplace smoothing (pseudocount) added to handle unobserved bins at small n.
    """
    rounded = np.round(samples).astype(int)
    counts  = np.zeros(K)
    for val in rounded:
        idx = int(val)  # bin index = integer value (since bins are [0,1), [1,2), ...)
        if 0 <= idx < K:
            counts[idx] += 1
    smoothed = counts + smoothing
    return smoothed / smoothed.sum()


def tvd(p, q):
    return 0.5 * float(np.sum(np.abs(p - q)))


def kl_divergence(true_masses, est_masses, eps=1e-10):
    """KL(true || estimated)."""
    est_safe = np.clip(est_masses, eps, None)
    mask = true_masses > 0
    return float(np.sum(true_masses[mask] * np.log(true_masses[mask] / est_safe[mask])))


def param_label(dist_name, params):
    if dist_name == "normal":
        return f"mean={params['mean']}, variance={params['variance']}"
    elif dist_name == "lognormal":
        return f"mu={params['mu']}, sigma={params['sigma']}"
    elif dist_name == "weibull":
        return f"scale={params['scale']}, shape={params['shape']}"
    return ""


# ---------------------------------------------------------------------------
# plotting
# ---------------------------------------------------------------------------

def plot_distribution(samples, emp_pmf_vals, true_pmf_vals, dist_name, dist_config, n, kl_val, tvd_val):
    """
    Bar chart showing empirical PMF (rounded samples) vs true discretised PMF.
    Zoomed to the region where the distribution has meaningful mass.
    """
    params = dist_config["params"]
    color  = dist_config["color"]

    # zoom to the region with non-negligible probability
    significant = np.where(true_pmf_vals > 0.001 * true_pmf_vals.max())[0]
    lo = max(0, significant[0]  - 2)
    hi = min(K, significant[-1] + 3)

    x = BIN_CENTRES[lo:hi]

    fig, ax = plt.subplots(figsize=(8, 4))

    bar_width = 0.4
    ax.bar(x - bar_width / 2, emp_pmf_vals[lo:hi], width=bar_width,
           color=color, alpha=0.7, label=f"Empirical PMF (n={n:,})")
    ax.bar(x + bar_width / 2, true_pmf_vals[lo:hi], width=bar_width,
           color="black", alpha=0.4, label="True PMF (binned)")

    ax.set_title(
        f"{dist_config['label']} — n = {n:,}\n"
        f"{param_label(dist_name, params)}    "
        f"KL = {kl_val:.5f}    TVD = {tvd_val:.5f}"
    )
    ax.set_xlabel("Delay (ms, rounded to nearest integer)")
    ax.set_ylabel("Probability mass")
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3, axis="y")

    output_path = os.path.join(RESULTS_DIR, f"dist_{dist_name}_n{n}.png")
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  Saved: {output_path}")


def plot_metrics_vs_n(dist_name, dist_config, ns, kl_values, tvd_values):
    """KL and TVD vs n on the same axes for one distribution."""
    color = dist_config["color"]

    fig, ax1 = plt.subplots(figsize=(7, 5))

    ax1.plot(ns, kl_values, "o-", color=color, linewidth=2,
             markersize=7, label="KL divergence")
    ax1.set_ylabel("KL divergence", color=color)
    ax1.tick_params(axis="y", labelcolor=color)

    ax2 = ax1.twinx()
    ax2.plot(ns, tvd_values, "s--", color="black", linewidth=1.8,
             markersize=6, label="TVD")
    ax2.set_ylabel("TVD", color="black")

    ax1.axvline(x=n_theory, color="grey", linestyle=":",
                linewidth=1.5, label=f"n_theory = {n_theory:,}")

    ax1.set_xscale("log")
    ax1.set_xlabel("Number of samples (n)")
    ax1.set_title(
        f"KL and TVD vs n — {dist_config['label']}\n"
        f"{param_label(dist_name, dist_config['params'])}  (empirical PMF, no KDE)"
    )

    # combined legend
    lines1, labels1 = ax1.get_legend_handles_labels()
    lines2, labels2 = ax2.get_legend_handles_labels()
    ax1.legend(lines1 + lines2, labels1 + labels2, fontsize=9)
    ax1.grid(True, alpha=0.3, which="both")

    output_path = os.path.join(RESULTS_DIR, f"metrics_{dist_name}.png")
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  Saved: {output_path}")


# ---------------------------------------------------------------------------
# main
# ---------------------------------------------------------------------------

def run():
    print_parameters()

    header = f"{'Distribution':<12} {'n':>10} {'KL':>12} {'TVD':>10}"
    print(header)
    print("-" * len(header))

    for dist_name, dist_config in DISTRIBUTIONS.items():
        true_pmf_vals = true_pmf(dist_name, dist_config["params"])

        ns         = []
        kl_values  = []
        tvd_values = []
        first_row  = True

        for n in SAMPLE_SIZES:
            samples       = load_samples(dist_name, n)
            emp_pmf_vals  = empirical_pmf(samples)
            kl_val        = kl_divergence(true_pmf_vals, emp_pmf_vals)
            tvd_val       = tvd(true_pmf_vals, emp_pmf_vals)

            label = dist_name if first_row else ""
            print(f"{label:<12} {n:>10,} {kl_val:>12.6f} {tvd_val:>10.6f}")
            first_row = False

            plot_distribution(samples, emp_pmf_vals, true_pmf_vals,
                              dist_name, dist_config, n, kl_val, tvd_val)

            ns.append(n)
            kl_values.append(kl_val)
            tvd_values.append(tvd_val)

        plot_metrics_vs_n(dist_name, dist_config, ns, kl_values, tvd_values)
        print()


if __name__ == "__main__":
    run()
